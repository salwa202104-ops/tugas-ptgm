a = 10
b = 3
# operasi pemangkatan **
hasil = a ** b
print(a,'**',b,'=',hasil)

# operator ==
hasil = a == b
print(a,'==',b,'=',hasil)

# operator !=
hasil = a != b
print(a,'!=',b,'=',hasil)

# operator >
hasil = a > b
print(a,'>',b,'=',hasil)

# operator <
hasil = a < b
print(a,'<',b,'=',hasil)

# operator >=
hasil = a >= b
print(a,'>=',b,'=',hasil)

# operator <=
hasil = a <= b
print (a,'<=',b,'=',hasil)